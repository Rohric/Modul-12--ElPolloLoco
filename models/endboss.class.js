  takeDamage(amount = 20) {
    if (this.dead) {
      return;
    }
    this.energy -= amount;
    if (this.energy <= 0) {
      this.energy = 0;
      this.dead = true;
    } else {
      this.lastHit = new Date().getTime();
    }
  }
